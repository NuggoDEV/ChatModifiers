/// <summary>
/// Represents different areas for chat modifiers.
/// </summary>
namespace ChatModifiers.API
{
    /// <summary>
    /// Enumeration defining various areas for chat modifiers.
    /// </summary>
    public enum Areas
    {
        /// <summary>
        /// No specific area.
        /// </summary>
        None,

        /// <summary>
        /// Menu area.
        /// </summary>
        Menu,

        /// <summary>
        /// Game area.
        /// </summary>
        Game,

        /// <summary>
        /// Both menu and game areas.
        /// </summary>
        Both
    }
}
